#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int i, char *as))
{
	char	*p_s;

	if (s == NULL || f == NULL)
		return ;
	p_s = s;
	while (*p_s != '\0')
	{
		(*f)(p_s - s, p_s);
		p_s++;
	}
}
